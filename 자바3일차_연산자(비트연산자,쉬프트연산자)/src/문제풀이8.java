
public class 문제풀이8 {
	public static void main(String[] arg) 
	{
	    int num1=10, num2=10;
	    System.out.printf("%d \n", ++num1);//num1을 1증가한 후 출력 
	                             // 11 ==> num1=11
	    System.out.printf("%d \n", num1); //11
	    System.out.printf("%d \n", num2++);// 출력한후에 num2을 1증가
	    //         10 => 11
	    System.out.printf("%d \n", num2);//11
	}

}
